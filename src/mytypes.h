#ifndef   __mytypes
#define   __mytypes

#include <stdbool.h>
#include <stdlib.h>

#ifndef __uint8_t__
typedef unsigned char uint8_t;
#endif //uint8_t

#ifndef __int8_t__
typedef signed char int8_t;
#endif //int8_t

#ifndef __uint16_t__
typedef unsigned short uint16_t;
#endif //uint16_t

#ifndef __int16_t__
typedef signed short int16_t;
#endif //uint16_t

#ifndef __uint32_t__
typedef unsigned long uint32_t;
#endif //uint32_t

#ifndef __int32_t__
typedef signed long int32_t;
#endif //int32_t

#ifndef __uint64_t__
typedef unsigned long long uint64_t;
#endif //uint64_t

#ifndef __int64_t__
typedef signed long long int64_t;
#endif //int64_t

#endif /* mytypes.h */
